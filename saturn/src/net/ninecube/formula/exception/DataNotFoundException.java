package net.ninecube.formula.exception;


/**
 * @author JXB
 */
public class DataNotFoundException extends FormulaException {

	public DataNotFoundException() {
		super();
	}

	public DataNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DataNotFoundException(String arg0) {
		super(arg0);
	}

	public DataNotFoundException(Throwable arg0) {
		super(arg0);
	}

}
