public class FrequenceException extends FormulaException {

	public FrequenceException() {
		super();
	}

	public FrequenceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FrequenceException(String arg0) {
		super(arg0);
	}

	public FrequenceException(Throwable arg0) {
		super(arg0);
	}

}
