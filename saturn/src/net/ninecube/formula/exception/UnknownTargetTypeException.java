package net.ninecube.formula.exception;

import net.ninecube.lang.BaseException;

/**
 * 
 * @author jxb
 * 
 */
public class UnknownTargetTypeException extends BaseException {

	public UnknownTargetTypeException() {
		super();
	}

	public UnknownTargetTypeException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnknownTargetTypeException(String message) {
		super(message);
	}

	public UnknownTargetTypeException(Throwable cause) {
		super(cause);
	}

}
